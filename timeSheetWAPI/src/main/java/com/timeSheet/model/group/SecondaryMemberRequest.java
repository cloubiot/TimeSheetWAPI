package com.timeSheet.model.group;

public class SecondaryMemberRequest {

	int groupId;
	int leadValue;
	int primaryValue;
	public int getGroupId() {
		return groupId;
	}
	public void setGroupId(int groupId) {
		this.groupId = groupId;
	}
	public int getLeadValue() {
		return leadValue;
	}
	public void setLeadValue(int leadValue) {
		this.leadValue = leadValue;
	}
	public int getPrimaryValue() {
		return primaryValue;
	}
	public void setPrimaryValue(int primaryValue) {
		this.primaryValue = primaryValue;
	}
	
	
}
